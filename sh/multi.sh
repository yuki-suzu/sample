#!/bin/bash

# ユーザーからの引数チェック (例: ./run-batch.sh 20)
MAX_RUNS=$1

if [ -z "$MAX_RUNS" ]; then
  echo "エラー: 起動数を引数として指定してください。 (例: $0 10)"
  exit 1
fi

LOG_DIR="./logs"
mkdir -p "$LOG_DIR"

# 起動した子シェルのPIDを管理する配列
PIDS=()

echo "========================================="
echo " 1 から $MAX_RUNS までの子シェルを非同期で一斉起動します"
echo "========================================="

# seq コマンドで 1 から MAX_RUNS までの連番ループを回す
for i in $(seq 1 "$MAX_RUNS"); do
  LOG_FILE="$LOG_DIR/task_$i.log"
  
  echo "[起動] 引数 $i でJarをバックグラウンド実行します..."
  
  # ( ) & を使って、ディレクトリ移動〜起動〜ログ吐き出しまでを1つの子シェルとして非同期化
  (
    # 必要であればここに cd などを挟む
    # --task.index などの引数名は、既存のJarが受け取る形に合わせて変更してください
    java -jar my-app.jar --task.index="$i" > "$LOG_FILE" 2>&1
  ) &
  
  # 子シェルのPIDを退避
  PIDS+=($!)
done

echo "========================================="
echo "すべてのプロセス（計 $MAX_RUNS 個）をバックグラウンドに投げました。"
echo "完了を待機しています... (Ctrl+C でスクリプトを抜けてもJavaは裏で動き続けます)"
echo "========================================="

# すべての子シェルが終了するのをメインシェルが待つ
wait "${PIDS[@]}"

echo "すべての非同期タスクが正常に終了しました。"
